package com.source.it.lecture1;

public class BooleanExample {
    public static void main(String[] args) {
        boolean b = false;
        boolean c = true;
        System.out.println(b & c);
        System.out.println(b && c);

        System.out.println(b | c);
        System.out.println(b || c);
        System.out.println(b ^ c);

        System.out.println(b == c);
        System.out.println(b != c);
    }
}
